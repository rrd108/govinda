<h1>Puspanna rizs</h1>

<p>A puspanna az egyik legszínesebb rizs. Ahogy a neve is utal rá, olyan, mint egy színes virágcsokor.
    Ünnepi alkalmakra ideális fogás.</p>

<h2>Hozzávalók</h2>
<ul>
    <li>4 szál sáfrány</li>
    <li>4 dl víz</li>
    <li>2 kk só</li>
    <li>½ kk őrölt szerecsendió</li>
    <li>5 ek olaj</li>
    <li>8 kesudió</li>
    <li>2 ek mandulaforgács</li>
    <li>2 ek mazsola</li>
    <li>egy kisebb darab egész fahéj</li>
    <li>1 kk római kömény</li>
    <li>3 egész kardamom</li>
    <li>¼ kk őrölt feketebors</li>
    <li>3 szegfűszeg</li>
    <li>¼ kk aszafoetida</li>
    <li>2 ek reszelt kókusz</li>
    <li>20 dkg rizs</li>
    <li>10 dkg főtt zöldborsó</li>
    <li>20 dkg panir</li>
</ul>

