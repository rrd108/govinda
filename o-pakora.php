<h1>Pakora</h1>

<p>Pakora szinte minden zöldségfajtából készíthető, mégis a karfiolpakora a legnépszerűbb. A városi
    utcáktól kezdve a vasútállomásokig mindenhol megszokott látvány egy-egy kis pakorás kézikocsit
    körülvevõ csoport. Könnyedén elkészíthető sós csemege váratlan vendégeknek, vagy paradicsom
    csatnival tálalva ízletes vacsora a családnak.</p>

<h2>Hozzávalók</h2>
<ul>
    <li>20 dkg csicseriborsó liszt</li>
    <li>2 kávéskanál kalinji (fekete hagymamag, nigella)</li>
    <li>1/4 kávéskanál chilipor</li>
    <li>2 kávéskanál õrölt koriander</li>
    <li>2 kávéskanál õrölt római kömény</li>
    <li>1 1/2 kávéskanál kurkuma</li>
    <li>1/2 kávéskanál hing</li>
    <li>3 kávéskanál só</li>
    <li>2-2,5 dl hideg víz</li>
    <li>70 dkg zöldség (kisebb rózsákra szedett karfiol, vagy vékony karikákra vágott burgonya,
        padlizsán, cukkini, csillagtök, kaliforniai paprika, csíkokra vágott sárgarépa),
        vagy kockákra vágott házi sajt</li>
    <li>ghí vagy olaj a sütéshez</li>
</ul>

