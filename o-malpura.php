<h1>Malpura</h1>

<p>A malpura több szempontból is ideális édesség a nyári szezonban. Egyrészt a gyümölcsszezonban
    ízletes joghurtot készíthetünk friss gyümölcsből, másrészt a joghurt kellemesen hűt a
    nyári melegben.</p>

<h2>Hozzávalók</h2>
<ul>
    <li>20 dkg fehér liszt</li>
    <li>8 dkg porcukor</li>
    <li>1/2 kávéskanál sütőpor</li>
    <li>1,5-2 dl tej szükség szerint</li>
    <li>zsiradék (olaj vagy ghí) a sütéshez</li>
    <li>1 l joghurt</li>
    <li>10 dkg cukor</li>
    <li>25 dkg málna</li>
</ul>

