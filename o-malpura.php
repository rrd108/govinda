    <p>
        Egy tálban összekeverjük a lisztet, a sütőport és a porcukrot. Fokozatosan öntjük hozzá a tejet,
        miközben habverővel simára keverjük. Egészen sűrű palacsintatészta állagúnak kell lennie.
        Ha megvan, negyed órára félretesszük, hogy a tészta pihenjen.
        Közben fogunk egy nagy tálat, s összekeverjük benne a joghurtot és a cukrot. Megmossuk a málnát, a
        felét összenyomjuk és az egész szemekkel együtt a joghurthoz adjuk.
        Ha a tészta már pihent, serpenyőben felmelegítjük a zsiradékot, a tésztából kiveszünk egy
        teáskanálnyit, majd a bő, forró zsiradékba csúsztatjuk. Annyit teszünk az edénybe, amennyi
        kényelmesen elfér benne. Időnként megforgatjuk, 3-4 perc alatt aranyszínűre sütjük, s végül
        lecsöpögtetjük a fánkokat. Ha kissé kihűltek, a gyümölcsjoghurtba forgatjuk őket.
