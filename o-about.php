<h1>Rólunk</h1>

<h2>Govinda világszerte</h2>

<p>A Govinda éttermek a Magyarországi Krisna-tudatú Hívők Közössége - világszerte pedig a
    Krisna-tudatú Hívők Nemzetközi Közössége - saját márkájú étteremlánca.
    A Govinda éttermek megalakulásához erősen hozzájárult a '70-es évek spirituális fellendülése,
    a zöld és vegetáriánus mozgalmak erősödése, amely nagyban köszönhető az indiai utazó szerzetesnek,
    és a Nemzetközi Közösség alapítójának, Śrīla Prabhupādának. 
    Prabhupāda az erőszakmentességen nyugvó vegetarianizmus népszerűsítése céljából kérte tanítványait
    arra, hogy minden nagyobb városban alapítsanak vegetáriánus éttermeket, táplálkozási alternatívát
    nyújtva az egyre szaporodó húst felszolgáló éttermekkel szemben.
    A Govinda így a világ első nagyszabású vegetáriánus étterem-hálózata. 
    Ma, 50 évvel később már több mint 110 Govinda étterem népszerűsíti szerte a világon a tiszta,
    vegetáriánus ízeket.</p>

<h2>Kínálatunk</h2>

<p>A Govinda éttermek palettáján szerepelnek hagyományos indiai fogások, lakto-vegetáriánus és vegán ételek,
    amelyek hús-, hal-, tojás- és hagymamentesek. Így kínálatában mindig megtaláljuk:</p>

<ul>
    <li>a hüvelyesekből készült tápláló leveseket, dhālokat,</li>
    <li>a különféle zöldségszószokat, subjikat,</li>
    <li>a sült sós csemegéket, pakorákat,</li>
    <li>az emésztésjavító édes-csípős szószokat, csatnikat,</li>
    <li>köreteket, rizsféléket,</li>
    <li>édességeket,</li>
    <li>gyógyteákat és limonádékat.</li>
</ul>

<p>A gluténérzékenyek szintén jó eséllyel találnak itt maguknak gluténmentes fogásokat.</p>

<h2>Táplálkozási kultúra</h2>

<p>Az Ájurvéda, India ősi táplálkozás- és orvostudományának előírásai szerint a tányérunkon mind
    a hatféle íznek (savanyú, keserű, édes, sós, csípős, fanyar) és különféle színű ételeknek jelen
    kell lenniük, legalább a főétkezésben. A Govinda éttermek menükínálatában ezért mindig a teljes
    íz- és színpaletta szerepel, hogy igazán sokrétű és egészséges ételeket fogyaszthassunk.</p>

<p></p>A Govinda éttermekben a világ legtisztább főzési hagyományával találkozhatunk, ahol kiemelten
figyelnek az ételek és a körülmények magas minőségére, tisztaságára. Bárhol is járunk,
a Govinda éttermekben azonos sztenderdek mellett, azonos minőséget és széles kínálatot találunk,
így külföldön is a legjobb választás, hogy felkeresésük.<p>
